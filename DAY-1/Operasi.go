package main 


import "fmt"

func main (){
	
	fmt.Println("Hello World"); //akan menampilkan tulisan hello word
	fmt.Println("go" + "Lang\n\n"); // akan menampilkan tulisan go lang tanda + berfungsi sebagai penghubung
	
	fmt.Println ("2*2= ", 2*2); // akan melakukan operasi 2*2 dan ditampilkan
	fmt.Println ("7/3= ", 7.4/3.5); // akan melakukan operasi 7.4/3.5 dan ditampilkan
	
	fmt.Println (!true); //akan menampilkan hasil boolean yang merupakan negasi dari true
	fmt.Println (true && true); //akan menampilkan hasil boolean dan dengan nilan benar dan benar

}

